const month=4;
switch(month){
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("may");
        break;
    case 6:
        console.log("june");
        break;
    default:
        console.log("default value is matched");
        break;
}